const getCommodityList = (platform: string, commodityId: string) => {
    let commodityList;

    switch (platform) {
        case 'android':
            commodityList = androidCommodities;
            break;
        case 'ios':
            commodityList = iosCommodities;
            break;
        default:
            throw new Error('Unsupported platform');
    }

    const commodity = commodityList[commodityId];

    if (!commodity) {
        throw new Error(`Commodity ID ${commodityId} not found for platform ${platform}`);
    }

    return {
        starCount: commodity.starCount,
        raiseCount: commodity.raiseCount,
        price: commodity.price
    };
};

export default getCommodityList;